package com.payconiq.apitests;

/**
 * This class provides some common HTTP response codes constants.
 * */
public class ResponseCodes {

    /**
     * variable for storing SUCCESS response code.
     * */
    public static final int SUCCESS = 200;

    /**
     * variable for storing CREATED response code.
     * */
    public static final int CREATED = 201;

    /**
     * variable for storing BADREQUEST response code.
     * */
    public static final int BADREQUEST = 400;

    /**
     * variable for storing METHODNOTALLOWED response code.
     * */
    public static final int METHODNOTALLOWED = 405;

    /**
     * variable for storing SERVERERROR response code.
     * */
    public static final int SERVERERROR = 500;
}
